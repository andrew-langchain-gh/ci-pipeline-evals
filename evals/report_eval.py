"""Generate a markdown evaluation report from LangSmith experiment results.

Reads evaluation_config__*.json artifacts produced by run_eval.py, queries
LangSmith for actual scores, and writes a markdown report suitable for posting
as a GitHub PR comment.

Usage:
    uv run python evals/report_eval.py
"""

import glob
import json
import operator
import os

from langsmith import Client

# Map string operators to functions for threshold comparison
OPERATOR_MAP = {
    ">=": operator.ge,
    "<=": operator.le,
    ">": operator.gt,
    "<": operator.lt,
    "==": operator.eq,
}


def parse_criterion(expression: str) -> tuple:
    """Parse a criterion like '>=7' into (operator_fn, threshold_value)."""
    for op_str in sorted(OPERATOR_MAP.keys(), key=len, reverse=True):
        if expression.startswith(op_str):
            value = float(expression[len(op_str):])
            return OPERATOR_MAP[op_str], value
    raise ValueError(f"Could not parse criterion: {expression}")


def main():
    client = Client()
    config_files = sorted(glob.glob("evaluation_config__*.json"))

    if not config_files:
        print("No evaluation config files found.")
        return

    report_lines = [
        "## Evaluation Report",
        "",
        "| Experiment | Metric | Avg Score | Threshold | Status |",
        "|------------|--------|-----------|-----------|--------|",
    ]

    all_passed = True

    for config_path in config_files:
        with open(config_path) as f:
            config = json.load(f)

        experiment_name = config["experiment_name"]
        criteria = config.get("criteria", {})

        # Fetch runs for this experiment
        runs = list(client.list_runs(project_name=experiment_name))

        if not runs:
            report_lines.append(
                f"| {experiment_name} | - | No runs found | - | :warning: |"
            )
            continue

        # Fetch feedback (scores) for all runs
        run_ids = [r.id for r in runs]
        feedback_list = list(client.list_feedback(run_ids=run_ids))

        # Group scores by feedback key
        scores_by_key: dict[str, list[float]] = {}
        for fb in feedback_list:
            if fb.score is not None:
                scores_by_key.setdefault(fb.key, []).append(fb.score)

        # Compare each metric against its criterion
        for metric_key, criterion_expr in criteria.items():
            op_fn, threshold = parse_criterion(criterion_expr)
            metric_scores = scores_by_key.get(metric_key, [])

            if not metric_scores:
                report_lines.append(
                    f"| {experiment_name} | {metric_key} | No scores | "
                    f"{criterion_expr} | :warning: |"
                )
                all_passed = False
                continue

            avg = sum(metric_scores) / len(metric_scores)
            passed = op_fn(avg, threshold)
            status = ":white_check_mark:" if passed else ":x:"

            if not passed:
                all_passed = False

            report_lines.append(
                f"| {experiment_name} | {metric_key} | {avg:.2f} | "
                f"{criterion_expr} | {status} |"
            )

    report_lines.append("")
    if all_passed:
        report_lines.append("**All evaluation criteria passed.**")
    else:
        report_lines.append("**Some evaluation criteria failed.** See details above.")

    report_lines.append("")
    report_lines.append(
        "_Results from [LangSmith](https://smith.langchain.com). "
        "Generated by CI pipeline._"
    )

    report = "\n".join(report_lines)

    output_path = os.environ.get("EVAL_REPORT_PATH", "eval_report.md")
    with open(output_path, "w") as f:
        f.write(report)

    print(report)
    print(f"\nReport written to {output_path}")


if __name__ == "__main__":
    main()
